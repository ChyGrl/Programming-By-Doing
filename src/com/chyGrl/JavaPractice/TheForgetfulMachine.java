package com.chyGrl.JavaPractice;

import java.util.Scanner;

public class TheForgetfulMachine {
	public static void main(String[] args) {
		
		Scanner keyboard = new Scanner(System.in);
		System.out.print("Tell me something good. ");
		keyboard.next();
		System.out.print("Tell me that you like it! ");
		keyboard.next();
		System.out.print("How many butts do you see in a day? ");
		keyboard.nextInt();
		System.out.print("How many times is enough? ");
		keyboard.nextInt();
	}
}
